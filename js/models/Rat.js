import BaseModel from './Base'





export default class Rat extends BaseModel {

  /******************************************************************************\
    Private Methods
  \******************************************************************************/

  _bindEvents () {
    this.listenTo(this, 'sync', () => {
      if (!this.get('loaded')) {
        this.set({
          loaded: true
        }, {
          silent: true
        })
      }
    })
  }





  /******************************************************************************\
    Public Methods
  \******************************************************************************/

  initialize () {
    this._bindEvents()
  }





  /******************************************************************************\
    Getters
  \******************************************************************************/

  get defaults () {
    return {
      loaded: false,
      loading: false
    }
  }
}
